c
c**********************************************
c       п/п обнуляет матрицу (а).
c**********************************************
c
c       n - номер матрицы
c       k - число строк
c       l - число столбцов
c
c
	subroutine obmatr(a,n,k,l)
	dimension a(10,4,4)
	do 10 j=1,k
	do 10 i=1,l
10      a(n,j,i)=0.
	return
	end
