c
c****************************************************
c       п/п умножения скаляр на вектор.
c****************************************************
c
c        q - скаляр
c       (а) - исходный вектор
c       (в) - результат
c        na - номер вектора (а)
c        nb - номер вектора (в)
c
c
	subroutine umsvek(q,a,b,nq,na,nb)
	dimension a(10,4,1),b(10,4,1),q(10)
	do 10 i=1,3
10      b(nb,i,1)=q(nq)*a(na,i,1)
	b(nb,4,1)=1.
	return
	end
